class Routes {
  // =============================================
  // ✨ 코어 경로 (인증 및 메인)
  // =============================================

  /// 앱의 시작점이자 메인 화면으로 이동하는 경로입니다.
  static const String main = '/';

  /// 로그인 화면으로 이동하는 경로입니다.
  static const String login = '/login';

  /// 회원가입 화면으로 이동하는 경로입니다.
  static const String signup = '/signup';

  // =============================================
  // 🎲 방 관련 경로
  // =============================================

  /// 방 목록 전체를 나타내는 기본 경로입니다.
  static const String rooms = '/rooms';

  /// 특정 방의 상세 화면으로 이동하는 경로입니다. (동적 ID 필요)
  /// 예: /rooms/abc-123
  static const String roomDetail = '/rooms/:id';

  /// 새로운 방을 생성하는 화면으로 이동하는 경로입니다.
  static const String createRoom = '/rooms/create';

  /// 기존 방을 찾는 화면으로 이동하는 경로입니다.
  static const String findRoom = '/rooms/find';

  // =============================================
  // ⚙️ 설정 관련 경로
  // =============================================

  /// 앱 설정 화면으로 이동하는 경로입니다.
  static const String options = '/options';

  // =============================================
  // 헬퍼 메서드 (경로 생성용)
  // =============================================

  /// 특정 방 ID를 받아 해당 방의 상세 경로를 완성하여 반환합니다.
  static String roomById(String id) => '/rooms/$id';
}
